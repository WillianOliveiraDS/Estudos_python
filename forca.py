
import random

def jogar():

    layout_abertura()
    palavra_secreta = gerando_palavra_secreta()
    letras_acertadas = inicializa_letras_acertadas(palavra_secreta)
    print(letras_acertadas)

    letras_faltando = str(letras_acertadas.count("_"))
    enforcou = False
    acertou = False
    erros = 0

    print("A palavra secreta tem {} letras".format(letras_faltando))

    while(not acertou and not enforcou):

        chute = chute_letra()


        if(chute in palavra_secreta):
            marcar_letra_acertada(palavra_secreta, chute, letras_acertadas)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)


    if(acertou):
        imprime_mensagem_ganho()
    else:
        imprime_mensagem_perdeu(palavra_secreta)


def layout_abertura():
    print("---------------------------------")
    print("   Bem vindo ao jogo da forca!")
    print("---------------------------------")


def chute_letra():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()
    return chute


def gerando_palavra_secreta():
    arquivo = open("palavra.txt", "r")
    palavras = []
    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)
    arquivo.close()

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta

def marcar_letra_acertada(palavra_secreta, chute, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1

